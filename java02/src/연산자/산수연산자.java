package 연산자;

public class 산수연산자 {

	public static void main(String[] args) {
		
		int x = 200;
		int y = 100;
		
		System.out.println("더한값은 "+ (x + y));
		System.out.println("뺀값은 "+ (x - y));
		System.out.println("곱한값은 "+ x * y);
		System.out.println("나눈값은 "+ x / y);
		System.out.println("나머지값은 "+ x % y);
		
	}
}
